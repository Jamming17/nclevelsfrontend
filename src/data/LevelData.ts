export interface LevelInterface {
    id: number;
    name: string;
    creator: string;
    difficulty: string;
    stars: number;
    rating: string;
    coin_count: number;
    coins_rated: boolean;
    length: string;
    extra: boolean;
    tags: string[];
}

export const EMPTY_LEVEL: LevelInterface = {
    id: -1,
    name: "",
    creator: "",
    difficulty: "",
    stars: -1,
    rating: "",
    coin_count: 0,
    coins_rated: false,
    length: "Tiny",
    extra: false,
    tags: []
}

export function levelInterfaceTypeGuard(obj: any): obj is LevelInterface {
    return (
        obj &&
        !isNaN(Number(obj.id)) &&
        typeof obj.name === "string" &&
        typeof obj.creator === "string" &&
        typeof obj.difficulty === "string" &&
        !isNaN(Number(obj.stars)) &&
        typeof obj.rating === "string" &&
        !isNaN(Number(obj.coin_count)) &&
        typeof obj.coins_rated === "boolean" &&
        typeof obj.length === "string" &&
        typeof obj.extra === "boolean" &&
        Array.isArray(obj.tags) &&
        obj.tags.every((tag: any) => typeof tag === "string")
    );
}

